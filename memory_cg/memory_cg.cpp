int32_t main(int32_t argc, char * argv[]) {
    fprintf(stdout, "[%d] start allocate memory\n", getpid());

    // 测试memory.use_hierarchy = 1，看cgroup会不会限制整个进程树，如果子进程一直在分配内存，会导致什么

    int32_t exitStatus = 0;
    pid_t pid = fork();

    switch(pid) {
        case -1:
            perror("fork()");
            exit(-1);
        case 0:
            printf("[Child] I'm Child process, Child's PID is %d, Parent PID is %d\n", getpid(), getppid());
            allocatMemory();
        default:
            printf("[Parent] I'm Parent process, Parent's PID is %d\n", getpid());
            wait(&exitStatus);
            printf("[Parent] Child's exit status is [%d]\n", WEXITSTATUS(exitStatus));
    }

    return 0;
}